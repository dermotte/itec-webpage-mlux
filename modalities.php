        <h1 id="text_documents">Text documents</h1>

        <div>

            <p>
                For all text documents authors have to ensure they are readable for all participants. For non editable
                text PDF should be preferred, for editable text either Microsoft Word 97-2003 format or the most recent
                LibreOffice format.
            </p>

        </div>
        <h1 id="emails">Emails</h1>

        <div>

            <p>
                For emails I recommend to:
            </p>
            <ul>
                <li>
                    <div> provide a meaningful subject line</div>
                </li>
                <li>
                    <div> send them in plain text (no <abbr title="HyperText Markup Language">HTML</abbr>,
                        RTF, or PDF with the content)
                    </div>
                </li>
                <li>
                    <div> send them with a minimum attachement size (e.g. resize images, use the “reduce
                        size” functions of PDF generators and text editors, etc.)
                    </div>
                </li>
                <li>
                    <div> follow the <a href="http://tools.ietf.org/html/rfc1855" rel="external"
                                                   title="http://tools.ietf.org/html/rfc1855"
                                                   rel="nofollow">netiquette</a> guidelines
                    </div>
                </li>
            </ul>

        </div>
